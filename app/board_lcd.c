/*********************************************************************
 * @fn      HexToAsciitodisplay
 *
 * @brief   Convert hex to string. Only needed when
 *          LCD display is used.
 *
 * @param   hex--hex adress  arraycount--hex count
 *
 * @return  str-Ascii adress
 */
char *HexToAsciitodisplay(uint8_t *hexarray,uint8_t arraycount)
{
  uint8_t     charCnt;
  char        hex[] = "0123456789ABCDEF";
  static char        str[50];
 // char       *str=(char *)malloc(2*arraycount+3);
  char        *pStr = str;

  *pStr++ = '0';
  *pStr++ = 'x';

  for (charCnt = arraycount; charCnt > 0; charCnt--)
  {
	*pStr++ = hex[*hexarray>> 4];
	*pStr++ = hex[*hexarray & 0x0F];
     hexarray++; 
  }
  pStr = NULL;

  return str;
}

